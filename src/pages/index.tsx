import type { NextPage } from "next";
import { signOut, useSession } from "next-auth/react";
import Head from "next/head";
import { useRouter } from "next/router";

const Home: NextPage = () => {
  const router = useRouter();

  const session = useSession();

  const hasUser = !!session.data?.user;

  console.log(session.data?.user);

  return (
    <div>
      <Head>
        <title>Next App - Login</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="container flex h-screen w-screen flex-col justify-center align-middle">
        <h1>Welcome to Dating Service</h1>
        {!hasUser && (
          <button
            onClick={() => router.push("/login")}
            className="bg-blue-400 px-10 py-2"
          >
            Login
          </button>
        )}
        {!!hasUser && (
          <button onClick={() => signOut()} className="bg-red-400 px-10 py-2">
            Logout
          </button>
        )}
      </main>
    </div>
  );
};

export default Home;
